// Theme provider component
export const MalPayThemeProvider: React.FC<{ children: React.ReactNode; darkMode?: boolean }> = ({ 
  children, 
  darkMode = false 
}) => {
  const theme = darkMode ? darkTheme : lightTheme;
  
  return (
    <ThemeProvider theme={theme}>
      <CssBaseline />
      {children}
    </ThemeProvider>
  );
};

